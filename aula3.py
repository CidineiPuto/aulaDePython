# Aspas simples.

print('Olá, meu nome é JUAN.')

# Aspas dupla.

print("DAORA MAN KKKK") 

# Escape

'''
O escape, serve caso você queira colocar MAIS de uma aspas dupla ou simples,
apenas colocar o dígito \ para colocar tal aspas.
'''

print("E qual é o seu nome ? \" me chamo Cid\"")


'''
O carácter de escape serve  para "escapar" do carácter, o pulando, como se ele fosse apenas
um texto normal. 
'''

# r

'''
Usando o comand r irá fazer com que após você fazer um código usando o escape,
o r fará as \ aparecerem lá.
'''

print(r'\'Nome legal mano.\'')


'''
Mas caso não queira complicar, só usar a aspas duplas na aspas simples, ou vice versa.
'''

print('Falou mano, foi "legal" te conhecer.')
print("Digo o 'mesmo'.")

